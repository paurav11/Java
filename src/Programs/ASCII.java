package Programs;

public class ASCII {
    public static void main(String[] args) {
        char ch1 = 'a';
        char ch2 = 'P';
        System.out.println("The ASCII value of " + ch1 + " is " + (int) ch1 + ".");
        System.out.println("The ASCII value of " + ch2 + " is " + (int) ch2 + ".");

        // Converting ASCII value to char
        System.out.println("The char value of " + 97 + " is " + (char)97 + ".");
        System.out.println("The char value of " + 80 + " is " + (char)80 + ".");
    }
}
